# Variables and Assignment Statements
x = 7
y = 3
print(x+y)

total = x+y
print(total)

name1 = "Sabbir"
name2 = "Shorna"
print(name1, name2)
print("\n")


# Types
x = 10
print(type(x))
x = 10.5
print(type(x))
x = "Sabbir"
print(type(x))
print("\n")


# Arithmetic operators
multiplication = 7 * 4
print(multiplication)

add = 10 + 15
print(add)

sub = 40 - 8
print(sub)

exponentiation = 2**10
print(exponentiation)

# True Division (/) vs. Floor Division (//)
True_Division = 7.4
print(True_Division)
Floor_Division = 7//4
print(Floor_Division)

remainder = 15 % 7
print(remainder)
print("\n")


# Relational Operators
x = 105
y = 100

print(x > y)
print(x < y)
print(x == y)
print(x != y)
print(x >= y)
print(x <= y)
print("\n")

# Chaining Comparisons
x = 3
y = 10

print(1 <= x <= 5)
